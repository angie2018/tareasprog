    private void jButtonSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSumaActionPerformed
        // TODO add your handling code here:
        if (jTxtResultados.getText().equals("")) {
            jTxtResultados.setText("0.0");
        }
        
        numero = Double.parseDouble(jTxtResultados.getText());
        operacion = "+";
        
        if (operacion.equals("-")){
            resultado = (resultado - numero);
            jTxtResultados.setText("");
        } else if (operacion.equals("*")){
            resultado = (resultado * numero);
            jTxtResultados.setText("");
        } else if (operacion.equals("/")){
            resultado = (resultado / numero);
            jTxtResultados.setText("");
        } else if (operacion.equals("x2")){
            resultado = (numero * numero);
            jTxtResultados.setText(Double.toString(resultado));
        } else if (operacion.equals("rz")){
            resultado = Math.sqrt(numero);
            jTxtResultados.setText(Double.toString(resultado));
        }

        operacion = "+";
        resultado = (resultado + numero);
        jTxtResultados.setText("");

    }//GEN-LAST:event_jButtonSumaActionPerformed

